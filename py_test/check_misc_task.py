def update_activity(activity):
    try:
        if not activity:
            return None
        al = activity.get('1')
        if not al:
            return None
        for item in al:
            if not item:
                continue
            if item.get('0') != 'discordActivity':
                continue

            k1 = item.get('1')
            if not k1:
                continue
            if k1 == 'true':
                item['1'] = True
                return activity
            if k1 == 'false':
                item['1'] = False
                return activity
        return None
    except Exception as e:
        print('update_activity: ', e)


def update_challenge(challenge):
    if not challenge:
        return None

    otk = challenge.get('12')
    if not otk:
        return None

    update = False
    result = []
    for item in otk:
        if not item:
            update = True
            continue

        item_id = item.get('id')
        if item_id:
            new_item = {'1': item_id, '2': item.get('count')}
            result.append(new_item)
            update = True

        else:
            result.append(item)

    if update:
        challenge['12'] = result
        return challenge
    return None


