def check_one(c):
    if not c.misc:
        return

    m = json.loads(c.misc)
    activity = m.get('21')
    if activity:
        data = activity.get('1')
        for item in data:
            if not item:
                continue
            if item.get('0') != 'dfw_model_500':
                continue
            shop = item.get('4')
            times = 0
            for _, v in shop.items():
                times += v.get('level', 0)

            acture = 0
            task = m.get('30')
            for k, v in task.items():
                if k in ('dfw_196f18711e8', 'dfw_196f1871679', 'dfw_196d302a75e', 'dfw_196d302b0b5', 'dfw_196d302b355'):
                    acture = max(v.get('current', 0), acture)
            if acture < times:
                print(f"Character ID: {c.database_id}")
                return


